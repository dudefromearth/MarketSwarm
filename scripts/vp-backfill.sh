    echo "4) Delete + FULL rebuild (max history)"
    echo "5) Show current summary"
    echo ""
    echo "x) Exit"
    echo ""

    read -rp "> " choice

    case "$choice" in
        1)
            echo "You chose: backfill last 5 years"
